package OOPLeason12_13.Task1;

public class Raccoon extends Animal {

    @Override
    void Voice() {
        System.out.println("Скрекоче");
    }

    @Override
    void Food() {
        System.out.println("Їсть все навіть зі смітника");
    }

    @Override
    void Kind() {
        System.out.println("Косумельскій єнот");
    }

    @Override
    void Breed() {
        System.out.println("Полоскун");
    }

    @Override
    void HaveTail() {
        System.out.println("Має довгий хвіст");
    }

    @Override
    void HaveTeeth() {
        System.out.println("Має гострі зуби");
    }

    @Override
    void NumberOfPaws() {
        System.out.println("Має чотири лапки дві з них мають хапальні рефлекси");
    }

    @Override
    void FriendshipToPeople() {
        System.out.println("Дружній до людей");
    }
}
