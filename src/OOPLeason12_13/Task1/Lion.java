package OOPLeason12_13.Task1;

public class Lion extends Animal {
    @Override
    void Voice() {
        System.out.println("голосно ричить");
    }

    @Override
    void Food() {
        System.out.println("Харчується м'яйсом ");
    }

    @Override
    void Kind() {
        System.out.println("Кошачі");
    }

    @Override
    void Breed() {
        System.out.println("Королівський");
    }

    @Override
    void HaveTail() {
        System.out.println("Має хвіст");
    }

    @Override
    void HaveTeeth() {
        System.out.println("Має гострі зуби");
    }

    @Override
    void NumberOfPaws() {
        System.out.println("Має масивні лапи");
    }

    @Override
    void FriendshipToPeople() {
        System.out.println("Небезпечний для людей");
    }
}


